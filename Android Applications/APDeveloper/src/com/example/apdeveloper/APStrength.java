public class APStrength {

	String mStrength;
	String mName;
	
	//Functions to set the values.
	public void setStrength(String value){
		mStrength = value;
	}
	
	public void setName(String value){
		mName = value;
	}
	
	//Functions to get the values.
	public String getStrength(){
		return mStrength;
	}
	public String getName(){
		return mName;
	}


}
